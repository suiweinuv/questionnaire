package com.model;

public class Xzl {
	private int id;
	private String title;
	private int area;
	
	
	public Xzl() {
		super();
	}


	public int getId() {
		return id;
	}


	public void setId(int id) {
		this.id = id;
	}


	public String getTitle() {
		return title;
	}


	public void setTitle(String title) {
		this.title = title;
	}


	public int getArea() {
		return area;
	}


	public void setArea(int area) {
		this.area = area;
	}


	public Xzl(int id, String title, int area) {
		super();
		this.id = id;
		this.title = title;
		this.area = area;
	}
	
	

	
}
